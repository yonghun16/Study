# iterable(반복/열거가능)하다는 것은 그 item들을 한 개씩 꺼내볼 수 있다는 것을 의미한다.
  # str, list, dict, tuple, set
# mutable 하다는 것은 부분적인 변경이 '가능'하다는 것을 의미한다.
  # list, set, dict
# immutable, 즉 부분적인 변경이 '불가능' 하다.
  # int, float, bool, str, tuple


print( type( 2) )
print( type(3.3) )
print( type(True) )
print( type(1j) )
print( type('python') )
print( type([1, 'hi', True]) )
print( type({'key1': 'value1', 'key2': 'value2'}) )
print( type((1, 'hi', True)) )
