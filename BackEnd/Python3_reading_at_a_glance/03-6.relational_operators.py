# 관계연산자 = 두 값의 대소 혹은 동등의 관계를 따지는 연산자
# == 두 값이 같은지 비교
# < 오른쪽 값이 왼쪽 값보다 더 큰지 비교 

print("100 == 100 :", 100 == 100)
print("100 == 200 :", 100 == 200)
print("100 != 100 :", 100 != 100)
print("100 != 200 :", 100 != 200)
print("0 < 9 :", 0 < 9)
print("0 > 9 :", 0 > 9)
print("0 >= 9 :", 0 >= 9)
print("0 <= 0 :", 0 <= 0)