/* 부정(Negation Operator) */
console.log("부정(Negation Operator)")

console.log(!true)   // true
console.log(!false)  // false
console.log(!0)
console.log(!!0)
console.log(!!!0)
console.log()

console.log(!null)
console.log(!undefined)
console.log(!NaN)
console.log(!'')
console.log()

console.log(!{})
console.log(![])
console.log()

